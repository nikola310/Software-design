/**
 * Paket u kome su implementirane mape i iterator za cuvanje podataka i prolaz kroz isti.
 */
package podaci;

/**
 * <code>IteratorMape</code> je interfejs iteratora koji prolazi kroz mapu.
 * @author Zivko Stanisic
 *
 */
public interface IteratorMape {
	/**
	 * Proverava da li je poslednji cklan u mapi.
	 * @return
	 */
	public boolean imaSledeceg();
	
	/**
	 * Vraca sledeci objekat u mapi.
	 * @return
	 */
	public Object sledeci();

}
